extension ViewController: UICollectionViewDataSource {
	func collectionView(_ collectionView: UICollectionView, numberOfItemsInSection section: Int) -> Int {
		return backgroundColors.count
	}
	
	func collectionView(_ collectionView: UICollectionView, cellForItemAt indexPath: IndexPath) -> UICollectionViewCell {
		let cell = collectionView.dequeueReusableCell(withReuseIdentifier: "cell", for: indexPath)
		cell.backgroundColor = backgroundColors[indexPath.row]
		return cell
	}
}

extension ViewController: UICollectionViewDelegate {
	func scrollViewWillEndDragging(_ scrollView: UIScrollView, withVelocity velocity: CGPoint, targetContentOffset: UnsafeMutablePointer<CGPoint>) {
		collectionView.center(scrollView: scrollView, targetContentOffset: targetContentOffset, count: backgroundColors.count, layout: collectionView.collectionViewLayout as? UICollectionViewFlowLayout)
	}
}
